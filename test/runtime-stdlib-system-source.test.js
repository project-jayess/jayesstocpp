  assert.match(runtime, /async_schedule\(\[result, operation = std::move\(operation\)\]\(\) mutable \{/);
  assert.match(runtime, /async_resolve\(result, operation\(\)\);/);
  assert.match(header, /struct stream_state/);
  assert.match(runtime, /value stream_open_read_async\(const std::string& pathText\)/);
