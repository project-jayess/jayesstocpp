test("runtime time helpers stay monotonic and millisecond-based", () => {
  const header = getRuntimeHeaderSource();
  const runtime = getRuntimeCppSource();

  assert.match(header, /value time_millis\(\);/);
  assert.match(header, /value time_seconds\(const value& input\);/);
  assert.match(header, /value time_minutes\(const value& input\);/);
  assert.match(header, /value time_elapsed\(const value& start\);/);
  assert.match(header, /value time_format_duration\(const value& milliseconds\);/);
  assert.match(runtime, /double time_require_milliseconds\(const value& input, const std::string& message\)/);
  assert.match(runtime, /std::chrono::steady_clock::now\(\)\.time_since_epoch\(\)/);
  assert.match(runtime, /std::chrono::duration_cast<std::chrono::milliseconds>\(now\)\.count\(\)/);
  assert.match(runtime, /return time_require_milliseconds\(input, "Jayess time seconds expects a finite number"\) \* 1000\.0;/);
  assert.match(runtime, /return time_require_milliseconds\(input, "Jayess time minutes expects a finite number"\) \* 60000\.0;/);
  assert.match(runtime, /value time_elapsed\(const value& start\) \{/);
  assert.match(runtime, /value time_format_duration\(const value& milliseconds\) \{/);
  assert.match(runtime, /output << minutes << "m ";/);
});

