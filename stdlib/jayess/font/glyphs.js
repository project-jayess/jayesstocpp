function fallbackRows() {
  return defaultGlyphs()["?"];
}

function normalizeChar(char) {
  return char;
}

export function createBitmapFont(name, glyphs, metrics) {
  return {
    name: name,
    glyphs: glyphs,
    charWidth: metrics.charWidth,
    charHeight: metrics.charHeight,
    advance: metrics.advance === null ? metrics.charWidth + 1 : metrics.advance,
    baseline: metrics.baseline === null ? metrics.charHeight - 1 : metrics.baseline,
    lineHeight: metrics.lineHeight === null ? metrics.charHeight + 1 : metrics.lineHeight
  };
}

export function defaultBitmapFont() {
  return createBitmapFont(defaultFontName, defaultGlyphs(), {
    charWidth: 5,
    charHeight: 7,
    advance: 7,
    baseline: 6,
    lineHeight: 8
  });
}

function ensureRegistry() {
  if (registeredFonts === null) {
    registeredFonts = {};
    registeredFonts[defaultFontName] = defaultBitmapFont();
  }
  return registeredFonts;
}

export function registerBitmapFont(font) {
  if (font === null || font.name === null) {
    throw "jayess:font expected a font with a name";
  }
  if (font.glyphs === null && font.kind !== "vector-font") {
    throw "jayess:font expected a bitmap font with glyphs or a vector font handle";
  }
  ensureRegistry()[font.name] = font;
  return font;
}

export function bitmapFontByName(name) {
  var registry = ensureRegistry();
  if (name === null) {
    return registry[defaultFontName];
  }
  var font = registry[name];
  if (font === null) {
    return registry[defaultFontName];
  }
  return font;
}

export function setDefaultBitmapFont(name) {
  var registry = ensureRegistry();
  if (registry[name] === null) {
    throw "jayess:font cannot set unknown default font";
  }
  defaultFontName = name;
  return registry[name];
}

export function glyphRowsForFont(font, char) {
  if (char === "\n") {
    return [];
  }
  var normalized = normalizeChar(char);
  var glyphs = font.glyphs;
  if (glyphs === null) {
    var defaultRows = defaultGlyphs()[normalized];
    if (defaultRows === null) {
      return fallbackRows();
    }
    return defaultRows;
  }
  var rows = glyphs[normalized];
  if (rows === null) {
    return fallbackRows();
  }
  return rows;
}

export function glyphExistsForFont(font, char) {
  if (char === "\n") {
    return true;
  }
  var glyphs = font.glyphs;
  if (glyphs === null) {
    return defaultGlyphs()[normalizeChar(char)] !== null;
  }
  return glyphs[normalizeChar(char)] !== null;
}

export function glyphRows(char) {
  return glyphRowsForFont(defaultBitmapFont(), char);
}
